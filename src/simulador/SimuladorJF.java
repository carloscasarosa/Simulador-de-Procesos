    private void jBArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBArchivoActionPerformed
        JFileChooser fc= new JFileChooser();
        fc.showOpenDialog(null);
        File archivo = fc.getSelectedFile();
        try{
            FileReader fr= new FileReader(archivo);
            BufferedReader br = new BufferedReader(fr);
            DefaultTableModel modelo = (DefaultTableModel) jTableTanda.getModel();

            modelo.setColumnCount(6); // Definir el número de columnas

            String linea;
            while ((linea = br.readLine()) != null) {
            String[] datos = linea.split("\\s+"); // Dividir por uno o más espacios
            modelo.insertRow(0, datos); // Insertar la fila en la primera posición
            //modelo.addRow(datos);
        }
            jTableTanda.setModel(modelo); // Establecer el modelo en la tabla
            
            JOptionPane.showMessageDialog(null, "Archivo se cargo correctamente"); 
        }
        catch(Exception e){}

    }//GEN-LAST:event_jBArchivoActionPerformed

