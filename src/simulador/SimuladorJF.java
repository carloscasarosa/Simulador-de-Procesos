private void obtenerDatos() {
        DefaultTableModel modelo = (DefaultTableModel) jTableTanda.getModel();
        //int i=0;
        //int j=0;
        
       for (int i = 0; i < modelo.getRowCount(); i++) {
                String nombre = (String) modelo.getValueAt(i, 0);
                int tiempoArribo = Integer.parseInt(modelo.getValueAt(i, 1).toString());
                int rafagasCPU = Integer.parseInt(modelo.getValueAt(i, 2).toString());
                int duracionRafagaCPU = Integer.parseInt(modelo.getValueAt(i, 3).toString());
                int duracionRafagaES = Integer.parseInt(modelo.getValueAt(i, 4).toString());
                int prioridadExterna = Integer.parseInt(modelo.getValueAt(i, 5).toString());

                // Crea un nuevo proceso con los datos obtenidos
                Proceso.nuevoProceso(nombre, tiempoArribo, rafagasCPU, duracionRafagaCPU, duracionRafagaES, prioridadExterna);
 
        }
  
    }
    
    
    
